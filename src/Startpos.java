/*
    Startpos.java

    Was used for a starting position object to test later stages in the level. No longer used.
 */

public class Startpos { // keeps track of coordinates of the starting position of the player.
    public int x, y;
    public Startpos(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX() {return x;}
    public int getY() {return y;}
}
